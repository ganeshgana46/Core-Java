package com.Basics;

public class Eg2 {
	
	//variable : Holds the value or stores the value
	//Data Type : Its specify the different values and sizes that can be store in a variable.
	
	//static variable
	
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static char c;
	static boolean bb;
	public static void main(String[] args) {
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(bb);
	}

}
