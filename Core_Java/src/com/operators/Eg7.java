package com.operators;

public class Eg7 {

	public static void main(String[] args) {
		
		//Bitwise operators
		
		int a = 15;
		int b = 8;
		
		//Bitwise AND(&)
		
		System.out.println(a&b);//8
		
		//Bitwise OR(|)
		
		System.out.println(a|b);//15
		
		//Bitwise XOR(^)
		
		System.out.println(a^b);//7
	}

}
