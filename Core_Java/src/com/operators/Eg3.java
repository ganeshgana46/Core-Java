package com.operators;

public class Eg3 {

	public static void main(String[] args) {
		
		//Relational operators
		
		//Comparison operators(<,>,<=,>=)
		
		int a = 10;
		int b = 20;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		
		//Equality operators
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
